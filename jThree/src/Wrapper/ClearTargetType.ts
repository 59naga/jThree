/**
/**
* Enum type for representing ClearTargetType
* gl.COLOR_BUFFER_BIT,
* gl.DEPTH_BUFFER_BIT,
* gl.STENCIL_BUFFER_BIT
*/
enum ClearTargetType {
  ColorBits = 16384, // COLOR_BUFFER_BIT,
  DepthBits = 256, // DEPTH_BUFFER_BIT,
  StencilBits = 1024 // STENCIL_BUFFER_BIT
}

export = ClearTargetType;
